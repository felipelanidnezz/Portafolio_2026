/** Código Vue del playground — Arcade Invaders */
const SHARED_TAIL = [
  "",
  "function spawnAliens(): Alien[] {",
  "  const rows = 3, cols = 5",
  "  const aliens: Alien[] = []",
  "  for (let r = 0; r < rows; r++) {",
  "    for (let c = 0; c < cols; c++) {",
  "      aliens.push({ x: 36 + c * 44, y: 48 + r * 34, alive: true })",
  "    }",
  "  }",
  "  return aliens",
  "}",
  "",
  "function drawPlayer(ctx: CanvasRenderingContext2D) {",
  "  ctx.fillStyle = PLAYER_COLOR",
  "  ctx.beginPath()",
  "  ctx.moveTo(playerX.value, H - 22)",
  "  ctx.lineTo(playerX.value - 14, H - 6)",
  "  ctx.lineTo(playerX.value + 14, H - 6)",
  "  ctx.closePath()",
  "  ctx.fill()",
  "}",
  "",
  "function drawInvaders(ctx: CanvasRenderingContext2D) {",
  "  aliens.value.forEach((a) => {",
  "    if (!a.alive) return",
  "    ctx.fillStyle = INVADER_COLOR",
  "    ctx.fillRect(a.x - 11, a.y - 8, 22, 14)",
  "  })",
  "}",
  "",
  "function shoot() {",
  "  if (Date.now() - lastShot.value < 280) return",
  "  bullets.value.push({ x: playerX.value, y: H - 36, vy: -5.5 })",
  "  lastShot.value = Date.now()",
  "}",
  "",
  "function updateAliens() {",
  "  const alive = aliens.value.filter((a) => a.alive)",
  "  if (!alive.length) { nextLevel(); return }",
  "  alive.forEach((a) => { a.x += alienDir.value * speed.value })",
  "}",
  "",
  "function nextLevel() {",
  "  if (level.value >= MAX_LEVEL) { status.value = 'win'; return }",
  "  level.value++",
  "  aliens.value = spawnAliens()",
  "  speed.value += 0.08",
  "}",
  "",
  "function checkCollisions() {",
  "  bullets.value.forEach((b) => {",
  "    aliens.value.forEach((a) => {",
  "      if (!a.alive) return",
  "      if (Math.abs(b.x - a.x) < 16 && Math.abs(b.y - a.y) < 14) {",
  "        a.alive = false",
  "        score.value += 10",
  "      }",
  "    })",
  "  })",
  "}",
  "",
  "function gameLoop(ts: number) {",
  "  if (!canvasRef.value) return",
  "  const ctx = canvasRef.value.getContext('2d')!",
  "  ctx.clearRect(0, 0, W, H)",
  "  drawStars(ctx, ts)",
  "  drawInvaders(ctx)",
  "  drawPlayer(ctx)",
  "  updateAliens()",
  "  checkCollisions()",
  "  rafId = requestAnimationFrame(gameLoop)",
  "}",
  "",
  "function startGame() {",
  "  aliens.value = spawnAliens()",
  "  score.value = 0",
  "  lives.value = 3",
  "  status.value = 'playing'",
  "  rafId = requestAnimationFrame(gameLoop)",
  "}",
  "",
  "function onKeydown(e: KeyboardEvent) {",
  "  if (e.key === 'ArrowLeft') playerX.value -= 8",
  "  if (e.key === 'ArrowRight') playerX.value += 8",
  "  if (e.key === ' ') shoot()",
  "}",
  "",
  "onMounted(() => {",
  "  window.addEventListener('keydown', onKeydown)",
  "  startGame()",
  "})",
  "",
  "onUnmounted(() => {",
  "  cancelAnimationFrame(rafId)",
  "  window.removeEventListener('keydown', onKeydown)",
  "})",
  "</script>",
  "",
  "<template>",
  '  <div class="arcade-cabinet">',
  '    <header class="arcade-marquee">NEON INVADERS</header>',
  '    <div class="hud">',
  "      <span>SCORE {{ score }}</span>",
  "      <span>LIVES {{ lives }}</span>",
  "    </div>",
  '    <canvas ref="canvasRef" :width="W" :height="H" class="game-screen" />',
  '    <p v-if="status === \'ready\'" class="blink">PRESS SPACE</p>',
  "  </div>",
  "</template>",
  "",
  "<style scoped>",
  ".arcade-cabinet {",
  "  background: linear-gradient(180deg, #1a0a2e, #050505);",
  "  border: 3px solid #a855f7;",
  "  border-radius: 12px;",
  "  padding: 12px;",
  "  box-shadow: 0 0 40px rgba(168, 85, 247, 0.35);",
  "}",
  "",
  ".arcade-marquee {",
  "  font-family: monospace;",
  "  color: #f0abfc;",
  "  text-align: center;",
  "  letter-spacing: 0.3em;",
  "  text-shadow: 0 0 12px #e879f9;",
  "}",
  "",
  ".game-screen {",
  "  display: block;",
  "  width: 100%;",
  "  background: #030308;",
  "  border: 2px solid #34d399;",
  "  image-rendering: pixelated;",
  "}",
  "",
  ".hud {",
  "  display: flex;",
  "  justify-content: space-between;",
  "  font-family: monospace;",
  "  font-size: 10px;",
  "  color: #22d3ee;",
  "  margin: 8px 0;",
  "}",
  "",
  ".blink {",
  "  animation: blink 0.8s step-end infinite;",
  "  text-align: center;",
  "  color: #fde047;",
  "  font-size: 10px;",
  "}",
  "",
  "@keyframes blink {",
  "  50% { opacity: 0; }",
  "}",
  "</style>",
];

